// postfix & prefix !


#include <iostream>
using namespace std;

int main()
{

        int x =6;
        cout<<"\t  addition  : "<<endl;
        cout <<x++<<endl;  // x = 6
        cout <<x<<endl;   // x = 7    


        int y = 5;
        cout<<endl<<endl<<"\t subtraction operation : "<<endl;
        cout << y-- <<endl; // y = 5
        cout << y<<endl; // y = 4   

            cout << endl << endl<<"************************************"<<endl<<endl;

            cout << endl << endl<<""<<endl;

        int p =6;
         cout<<endl<<endl<<endl<<"\t  addition  : "<<endl;
        cout<< ++p <<endl;  // p = 7
        cout<< p <<endl;    // p = 7   


        int n =6;
            cout<<endl<<endl<<"\t subtraction operation : "<<endl; 
            cout << --n <<endl;  // n = 5
            cout << n <<endl;    // n = 5



    return 0;
}



