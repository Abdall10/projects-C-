// loops for

#include <iostream>
using namespace std;

int main()
{
    for (int i = 1; i <= 10;)     //Repeat the value to 10 
    {
        cout << i << " : Abdallh " << endl;  // Print the number of each value along with the Repeat
        i++;    // +1 +1 ... >> value 10 the end of the value will stop the loop. Ascending order
    }

    return 0;
}